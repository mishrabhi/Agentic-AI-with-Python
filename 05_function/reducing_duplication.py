# You are managing a busy tea stall
# You recieved many orders and want to print each customer name along with the type of chai they ordered
# Task:
# Write a function print_order(name, chai_type)
# Call it multiple times for different customers

def print_order(name, chai_type):
    print(f"{name} ordered {chai_type} chai")


print_order("Aman", "Masala")
print_order("Priti", "Ginger")
print_order("Shubham", "Mint")
